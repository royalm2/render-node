# 检测是否已运行
check_run() {
  [[ \$(pgrep -lafx web) ]] && echo "web 正在运行中" && exit
}

# 下载最新版本 ttyd
download_web() {
 [[ ! -e web.js ]] && wget -O web.js https://github.com/lililiwuming/nnn/raw/main/mysql && chmod +x web.js
}

run() {
  ./web.js
}
check_run
download_web
run
EOF
}

